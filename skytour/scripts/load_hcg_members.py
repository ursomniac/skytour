def get_object_type(morph):
    try:
        if not not_empty(morph):
            return ObjectType.objects.get(slug='galaxy--unclassified')
        elif morph[:2] == 'SB':
            return ObjectType.objects.get(slug='galaxy--barred-spiral')
        elif morph in ['E-S0', 'S0', 'S0-a']:
            return ObjectType.objects.get(slug='galaxy--lenticular')
        elif morph[0] == 'E':
            return ObjectType.objects.get(slug='galaxy--elliptical')
        elif morph[:2] == 'SA':
            return ObjectType.objects.get(slug='galaxy--intermediate')
        elif morph[0] == 'I':
            return ObjectType.objects.get(slug='galaxy--irregular')
        elif morph[0] == 'S':
            return ObjectType.objects.get(slug='galaxy--spiral')
        else:
            return ObjectType.objects.get(slug='galaxy--unclassified')
    except:
        print(f"MORPH ISSUE = [{morph}]")

def run_member(line, cat_dict, con_dict):
    fields = split_line(line)
    
    # ID
    id_in_list = fields[0]      # 54d
    hcgid = fields[0][:-1]      # 54 
    hcgindex = fields[0][-1]    # d
    parent = DSO.objects.get(catalog=cat_dict['HCG'], id_in_catalog=hcgid)
    # Catalog Alias
    catid = fields[1]
    catalog, id_in_catalog = catid.split(' ')
    alias_catalog = cat_dict[catalog]

    # already in DB
    in_db = not_empty(fields[2]) 
    if in_db:
        dso_in_field = get_dso(fields[2])
        # TODO: Handle this (or not?)
        return None
    else:
        # Coordinates
        ra, dec = get_radec(fields[3])
        con_loc = get_constellation(ra['value'], dec['value'])
        con_code = con_loc['abbr'].upper()
        my_constellation = con_dict[con_code]
        morph = fields[4]
        # Angsize
        d25 = float(fields[5]) if not_empty(fields[5]) else None
        r25 = float(fields[5]) if not_empty(fields[5]) else None
        if d25 and r25:
            amajor, aminor, angsize = get_size_from_logd25(d25, r25, both=True)
        else:
            amajor = aminor = angsize = None
        # Orientation
        ori = float(fields[7]) if not_empty(fields[7]) else None
        # Magnitude
        mag = float(fields[8]) if not_empty(fields[8]) else None
        # Surface Brightness
        sb = (float(fields[9])+8.89) if not_empty(fields[9]) else None
        # Distance
        dist = get_distance_from_modulus(float(fields[10])) if not_empty(fields[10]) else None

        # Create new DSOInField object
        f = DSOInField.objects.filter(catalog=cat_dict['HCG'], id_in_catalog=id_in_list).first()
        if f is None:
            f = DSOInField()
            create = True
        else:
            create = False
        f.catalog = cat_dict['HCG']
        f.id_in_catalog = id_in_list
        f.constellation = my_constellation
        f.object_type = get_object_type(morph)
        f.morphological_type = morph
        f.parent_dso = parent
        # coordinates
        f.ra_h = ra['h']
        f.ra_m = ra['m']
        f.ra_s = ra['s']
        f.ra = ra['value']
        f.dec_sign = dec['sign']
        f.dec_d = dec['d']
        f.dec_m = dec['m']
        f.dec_s = dec['s']
        f.dec = dec['value']
        # attributes
        f.magnitude = mag
        f.magnitude_system = 'B'
        f.angular_size = angsize
        f.major_axis_size = amajor
        f.minor_axis_size = aminor
        f.surface_brightness = sb
        f.orientation_angle = ori
        f.distance = dist
        f.distance_units = 'Mly'        
        # Save
        f.save()
        # Add Alias
        if create:
            alias = DSOInFieldAlias()
            alias.object = f
            alias.catalog = alias_catalog
            alias.id_in_catalog = id_in_catalog
            alias.save()
    return f

